package String;

public class StringReplaceChar {
	
	public static void main(String[] args) {
		
		String str3 = "Pakistan";
		
		String str4 = str3.replace("Pak" , "Turk");
		
		String str5 = str3.toLowerCase();
		String str6 = str3.toUpperCase();
		
		System.out.println(str3 + "\n" + str4 + "\n" + str5 + "\n" + str6);
		

	}

}
