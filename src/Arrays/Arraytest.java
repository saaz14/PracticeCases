package Arrays;

public class Arraytest {
	
	public static void main(String[] args) {
		
		int[] arr = new int[11];
		
		arr[0] = 0;
		arr[1] = 10;
		arr[2] = 20;
		arr[3] = 30;
		arr[4] = 40;
		arr[5] = 50;
		arr[6] = 60;
		arr[7] = 70;
		arr[8] = 80;
		arr[9] = 90;
		arr[10] = 100;
		
		
		for (int i = 0; i < arr.length; i++) {
			System.out.println("10 x " + i + " = " + arr[i]);
		}
		
	}

}
