package ClassAttributes;

public class MultiAttributes {
	
	String fname = "Aqeel";
	String lname = "Zaidi";
	int age = 36;

	public static void main(String[] args) {
		
		MultiAttributes Shah = new MultiAttributes();
		System.out.println("Name: " + Shah.fname + " " + Shah.lname);
		System.out.println("Age: " + Shah.age);

	}

}
