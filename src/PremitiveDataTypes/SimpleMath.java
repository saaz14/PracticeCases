package PremitiveDataTypes;

public class SimpleMath {

	public static void main(String[] args) {
		
		
		//Learning simple math equations	
		
		int a = 5;
		int b = 10;
		int c = a*b;
		
		int aa = 45;
		int bb = 50;
		int cc = aa+bb;
		
		int aaa = 80;
		int bbb = 65;
		int ccc = aaa-bbb;
		
		int aaaa = 150;
		int bbbb = 35;
		double cccc = (double)aaaa/bbbb;

		System.out.println("Multiply " + c);
		System.out.println("Addition " + cc);
		System.out.println("Subtraction " + ccc);
		System.out.println("Division " + cccc);

	}

}
